package com.booleanuk.core;

public class Item {
    private String sku;
    private double price;
    private String name;
    private String variant;
    private int size;

    public String getSku() {
        return sku;
    }

    public double getPrice() {
        return price;
    }

    public String getName() {
        return name;
    }

    public String getVariant() {
        return variant;
    }

    public int getSize() {
        return size;
    }
}
